"""
Приклад 5: Магія Python (Рядки, великі числа та генератори списків)

Мета: Показати, як Python може легко працювати з рядками та дуже великими числами.
"""

text = "Python це круто"
print(text[::-1])  # Переверне текст задом наперед
print(2 ** 10000)  # 2 у 10-тисячому ступені миттєво
print([x**2 for x in range(1, 21) if x % 2 == 0])  # Список квадратів парних чисел від 1 до 20 за допомогою генератора списків
print("Привіт, " + "світ!" * 3)  # Конкатенація рядків
