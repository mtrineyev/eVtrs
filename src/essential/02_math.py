"""
Приклад 2: Калькулятор пального (Математика та Логіка)

Мета: Реальний приклад розрахунку

Сюжет: Ви плануєте поїздку і хочете знати, скільки пального вам знадобиться
"""


distance = float(input("Скільки км треба проїхати? "))
consumption = float(input("Яка витрата пального на 100 км? "))

total_fuel = (distance / 100) * consumption
print(f"Вам знадобиться приблизно {total_fuel:.2f} літрів пального.")
