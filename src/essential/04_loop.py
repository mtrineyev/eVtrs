"""
Приклад 4: Легенда про шахівницю (Цикли)

Сюжет: Винахідник шахів попросив у раджу винагороду:
    за першу клітинку шахівниці — 1 зернятко, за другу — 2, за третю — 4 і так далі
    (кожна наступна вдвічі більше за попередню).
    Раджа згодився, але коли почав рахувати, то зрозумів, що зробив велику помилку.

Скільки зерняток потрібно для заповнення всієї шахівниці (64 клітинки)?

Мета: Показати, як використовувати цикли для розв'язання задачі,
    яка має експоненціальний ріст.
"""

total_grains = 0
grains_on_cell = 1

print("Починаємо підрахунок по клітинках (всього 64):")

for cell in range(1, 65):
    total_grains += grains_on_cell
    print(f"Клітинка {cell}: на ній {grains_on_cell} зернят. Всього: {total_grains}")
    
    # Подвоюємо кількість для наступної клітинки
    grains_on_cell *= 2

print("-" * 30)
print(f"Фінальна кількість зерна: {total_grains} шт.")
